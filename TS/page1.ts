//implicit declaration
const num1 = 10
console.log(`num1 = ${num1}, type = ${typeof num1}`)

const str = 'Hello'
console.log(`string = ${str}, type = ${typeof str}`)

//explicit declaration
//data type is declared by developer
const num2:number = 100
console.log(`num2 = ${num2}, type = ${typeof num2}`)